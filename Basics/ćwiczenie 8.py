wymiar1 = int(input("wymiar 1: "))
wymiar2 = int(input("wymiar 2: "))
wymiar3 = int(input("wymiar 3: "))
Objetosc = wymiar1 * wymiar2 * wymiar3
if Objetosc > 1000:
    print("Objętość wynosi:", Objetosc, "i jest wieksza niż 1000")
else:
    print("Objętość wynosi:", Objetosc, "i jest mniejsza niż 1000")




